package com.accenture.postgres.repository;

import com.accenture.postgres.models.DemoProfile;
import org.springframework.data.r2dbc.repository.Query;
import org.springframework.data.r2dbc.repository.R2dbcRepository;
import org.springframework.stereotype.Repository;
import reactor.core.publisher.Flux;

@Repository
public interface DemoRepo extends R2dbcRepository<DemoProfile, Integer> {

    @Query("SELECT * FROM demo_profile WHERE id = :id")
    Flux<DemoProfile> getRecordById(String id);
}
